

 class Demo {
    public static void main(String[] args) {
		
		int intvalue = 1000;
		float floatvalue = 20.2f;
		char charvalue = 'S';
		boolean booleanvalue = true;
		double doublevalue = 30.33;
		long longvalue = 222111222L;
		byte bytevalue = 12;
		short shortvalue = 1233;
		
		
		System.out.println("value is : " + intvalue);
		System.out.println("value is : " + floatvalue);
		System.out.println("value is : " + charvalue);
		System.out.println("value is : " + booleanvalue);
		System.out.println("value is : " + doublevalue);
		System.out.println("value is : " + longvalue);
		System.out.println("value is : " + bytevalue);
		System.out.println("value is : " + shortvalue);
		
		
    }
}